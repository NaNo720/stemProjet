
document.addEventListener("DOMContentLoaded", function() {
    const toggleElement = document.getElementById("toggleboxe");
    const closeButton = document.getElementById("closeButton");
    const tailleElement = document.getElementById("taille");

    tailleElement.addEventListener("click", function(event) {
        // Vérifier si l'élément cliqué a la classe "st0"
        if (event.target.classList.contains("st0")) {
            // Faire apparaître l'élément avec l'ID "toggle"
            toggleElement.style.display = "block";
        }
    });

    closeButton.addEventListener("click", function() {
        // Cacher l'élément avec l'ID "toggle"
        toggleElement.style.display = "none";
    });
});

